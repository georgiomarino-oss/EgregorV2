create or replace function public.track_mobile_analytics_event(
  p_event_name text,
  p_event_properties jsonb default '{}'::jsonb,
  p_event_source text default 'mobile_app',
  p_session_id text default null,
  p_platform text default null,
  p_app_version text default null,
  p_build_number text default null
)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  v_event_id bigint;
  v_event_name text := lower(btrim(coalesce(p_event_name, '')));
  v_event_properties jsonb := coalesce(p_event_properties, '{}'::jsonb);
  v_event_source text := coalesce(nullif(btrim(p_event_source), ''), 'mobile_app');
begin
  if v_event_name = '' then
    raise exception 'Event name is required.';
  end if;

  if char_length(v_event_name) > 120 then
    raise exception 'Event name exceeds max length.';
  end if;

  if v_event_name !~ '^[a-z0-9_]+$' then
    raise exception 'Event name contains unsupported characters.';
  end if;

  if jsonb_typeof(v_event_properties) is distinct from 'object' then
    raise exception 'Event properties must be a JSON object.';
  end if;

  if pg_column_size(v_event_properties) > 8192 then
    raise exception 'Event properties payload exceeds max size.';
  end if;

  insert into public.mobile_analytics_events (
    user_id,
    event_name,
    event_properties,
    event_source,
    session_id,
    platform,
    app_version,
    build_number
  )
  values (
    auth.uid(),
    v_event_name,
    v_event_properties,
    v_event_source,
    nullif(btrim(p_session_id), ''),
    nullif(btrim(p_platform), ''),
    nullif(btrim(p_app_version), ''),
    nullif(btrim(p_build_number), '')
  )
  returning id into v_event_id;

  return v_event_id;
end;
$$;

