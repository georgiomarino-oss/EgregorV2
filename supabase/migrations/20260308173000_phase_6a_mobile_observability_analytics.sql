create table if not exists public.mobile_analytics_events (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users(id) on delete set null,
  event_name text not null,
  event_properties jsonb not null default '{}'::jsonb,
  event_source text not null default 'mobile_app',
  session_id text,
  platform text,
  app_version text,
  build_number text,
  occurred_at timestamptz not null default timezone('utc', now()),
  created_at timestamptz not null default timezone('utc', now()),
  constraint mobile_analytics_events_event_name_length check (
    char_length(event_name) >= 1 and char_length(event_name) <= 120
  ),
  constraint mobile_analytics_events_event_source_check check (
    event_source in ('mobile_app')
  )
);

create index if not exists mobile_analytics_events_user_idx
on public.mobile_analytics_events(user_id, occurred_at desc);

create index if not exists mobile_analytics_events_name_idx
on public.mobile_analytics_events(event_name, occurred_at desc);

alter table public.mobile_analytics_events enable row level security;

